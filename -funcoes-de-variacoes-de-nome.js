function obtenhaNumeroDeCaracteres(nome){
  return nome.length
}

function obtenhaNomeMinusculo(nome){
  return nome.toLowerCase()
}

function obtenhaNomeMaiusculo(nome){
  return nome.toUpperCase()
}

console.log(obtenhaNumeroDeCaracteres("Fernanda"))
console.log(obtenhaNomeMinusculo("FeRnAnDa"))
console.log(obtenhaNomeMaiusculo("fernanda"))